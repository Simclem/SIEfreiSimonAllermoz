            <p>
                <b>ARTICLE 1 - Membres, cotisations</b></br>
                Seuls sont membres du club les personnes étant à jour de leur cotisation annuelle. Les cotisations annuelles sont valables à partir du 1er octobre pour la cotisation entière et du 1er avril pour la demi cotisation de chaque saison. Les montants de ces cotisations annuelles sont révisés chaque année et soumis à l'approbation de l'Assemblée générale ou du Comité de direction. Une preuve d’adhésion (attestation de licence, badge d’accès) doit pouvoir être présentée à toute requête des membres du bureau ou de toute personne mandatée.
            </p>
            <p>
                <b>ARTICLE 2 - Licence et assurance</b></br>
                Les membres du club sont licenciés à la Fédération Française de Tennis. L’attestation de licence est disponible uniquement en ligne dans l’espace licencié du site de la FFT (<a href="http://ww2.fft.fr/action/espace_licencies/">http://ww2.fft.fr/action/espace_licencies/</a>). Les membres bénéficient, à ce titre, d'une assurance les couvrant lors d'un accident. Cette assurance agit :</br>
                - en individuelle accident lorsque le licencié est victime au cours ou à l'occasion de la pratique du tennis (y compris au cours de déplacements, animations pour le compte du club),</br>
                - en responsabilité civile vis-à-vis des tiers, lorsque le licencié est l'auteur du dommage. Le licencié peut souscrire des garanties complémentaires s'il le souhaite.
            </p>
            <p>
                <b>ARTICLE 3 - Accès aux courts</b></br>
                1. Accès général :</br>
                L'accès aux courts est réservé aux membres de club. Il s'effectue à l'aide d’un badge d’accès individuel fourni sur demande contre caution lors de l’inscription.</br>
                2. Réservations :</br>
                Le système de réservation des courts s’appuie sur le service en ligne http://www.adoc.appli-fft.fr/adoc/ proposé par la fédération.</br>
                Les adhérents sont en mesure d’effectuer une seule réservation à la fois. Un adhérent devra donc attendre la fin de sa première réservation avant d’effectuer la suivante.</br>
                Les entrées et les sorties de joueurs des courts ont lieu pour les terrains n°1 (couvert et extérieur) à l’heure juste (ex: 13h00) et à la demie (ex: 10h30) sur les terrains n°2 (couvert et extérieur).</br>
                Ces règles s’appliquent aux plages horaires réservées au club (enseignement et compétition).</br>
                En cas d’affluence, la durée d’occupation des courts est limitée à 1 heure. Des réservations ponctuelles et exceptionnelles peuvent être effectuées à la diligence du Comité de direction (compétitions, enseignement, animations...)
            </p>
            <p>
                <b>ARTICLE 4 - École de tennis et déplacements</b></br>
                Avant de déposer leurs enfants au club, les parents doivent s'assurer qu'il y ait bien un responsable pour les accueillir. Cette disposition est valable même si les entraînements se déroulent dans un gymnase ou dans une salle situés hors de l'enceinte du club. Les enfants restent sous l'entière responsabilité des parents, sauf pendant la durée des cours, les enfants étant alors sous la responsabilité de l'éducateur. L'inscription d'un enfant à l'école de tennis entraîne l'autorisation parentale pour les déplacements occasionnés par cette activité (compétition, entraînement). Cette autorisation est à renouveler chaque année.
            </p>
            <p>
                <b>ARTICLE 5 - Tenue</b></br>
                Une tenue correcte et décente est de rigueur. Les chaussures de sport sont obligatoires et doivent être adaptées à la pratique du tennis ainsi qu'à la nature du sol.
            </p>
            <p>
                <b>ARTICLE 6 - Entretien</b></br>
                Les courts et les parties communes (accès, vestiaires) doivent être maintenues en parfait état de propreté.</br>
                Les adhérents sont encouragés à utiliser les brosses de nettoyage des semelles disponibles à l’entrée des courts.
            </p>
            <p>
                <b>ARTICLE 7 - Court couvert</b></br>
                Les boissons sucrées (type soda) et alcoolisées sont strictement interdites sur les courts couverts car elles sont susceptibles de tacher la surface en résine de façon irréversible.</br>
                L’usage de chaussures à semelle noire ou inadaptées à la pratique du tennis sont strictement interdites.
            </p>
            <p>
                <b>ARTICLE 8 - Discipline</b></br>
                Par égard aux autres joueurs il est formellement interdit de fumer, de cracher, ou de jeter des projectiles sur les courts. Tout acte visant à dégrader les courts sera passible de sanctions sous forme d’exclusion du club et/ou de poursuite. Toute autre activité que le tennis est interdite sur les courts, sauf activité organisée en accord avec le club. La présence d'animaux est interdite sur les courts.</br>
                Les membres du Comité de direction ont vocation à pénétrer sur les courts pour régler tout litige en suspens. En cas de faute grave d'un adhérent, le Comité de Direction peut procéder à sa radiation temporaire ou définitive. L'intéressé, invité à fournir ses explications préalablement à toute décision, pourra exercer un recours auprès de l'Assemblée Générale ordinaire, devant laquelle il bénéficiera des mêmes droits pour assurer sa défense.</br>
                Durant les cours, l'éducateur présent se réserve le droit de sanctionner tout comportement jugé incompatible avec l'enseignement dispensé. Ces sanctions peuvent prendre la forme d'une exclusion soit temporaire, soit définitive du contrevenant. En aucun cas les heures ainsi perdues ne seront remboursées.</br>
                Il est recommandé de ne pas laisser les enfants en bas âge sans surveillance, sur les courts ou dans l'enceinte du club.
            </p>
            <p>
                <b>ARTICLE 9</b></br>
                Le club décline toute responsabilité en cas de perte ou de vol sur les courts et dans les parties communes.
            </p>
            <p>
                <b>ARTICLE 10</b></br>
                L'adhésion au club entraîne l'acceptation de toutes les clauses du présent règlement.
            </p>
